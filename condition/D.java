package com.condition;
// it will run both do and while condition 
//while will execute until condition of given value(a=10) is true 
public class D {
	public static void main(String[]args) {
		int i=20;
		do {
			System.out.println(i);
			
		}while(i<10);
		
		
		int j=1;
		do {
			System.out.println(j);
			j++;
			
		}while(j<5);
	}
}
